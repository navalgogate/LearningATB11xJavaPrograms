package ex_01_JavaBasic;

public class Lab0013_Increment_Operator {
    public static void main(String[] args) {

     int a1 = 10; //Pre-increment
        System.out.println(++a1);
        System.out.println(a1);

        int a2 = 10; //Post-increment
        System.out.println(a2++);
        System.out.println(a2);


    }

}
