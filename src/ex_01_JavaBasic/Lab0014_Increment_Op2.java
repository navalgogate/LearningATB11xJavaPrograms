package ex_01_JavaBasic;

public class Lab0014_Increment_Op2 {
    public static void main(String[] args) {

        int a= 10;
        int result = ++a;

        System.out.println(a);
        System.out.println(result);


        int a1= 10;
        int result1 = a1++;

        System.out.println(a1);
        System.out.println(result1);


    }

}
