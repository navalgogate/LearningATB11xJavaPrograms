package ex_04_For_Loop;

public class Lab0034_For_Loop7 {
    public static void main(String[] args) {

        for (int i=0;i<=50;i++)
        {
            if(i%2==0)
            {
                System.out.println("Its even number:" +i);
            }
            else
            {
                System.out.println("Its Odd number:" +i);
            }
        }
    }
}
